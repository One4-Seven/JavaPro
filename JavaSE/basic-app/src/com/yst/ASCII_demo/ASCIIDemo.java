package com.yst.ASCII_demo;

public class ASCIIDemo {
    public static void main(String[] args) {
        // 彻底了解 ASCII 表的基本构造
        char a = 'a';
        char A = 'A';
        char o = '0';
        System.out.println(a + 10);
        System.out.println(A + 10);
        System.out.println(o + 10);
        // 二进制 八进制 十六进制
        int a1 = 0B110;
        int a2 = 0141;
        int a3 = 0XA10;
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
    }
}
