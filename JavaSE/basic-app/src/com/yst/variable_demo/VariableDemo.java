package com.yst.variable_demo;

public class VariableDemo {
    public static void main(String[] args) {
        // 变量基本类型 内存中占字节不同
        byte a = 127;
        short b = 13223;
        int c = 5201314;
        long d = 72786147821L;
        float e = 3.14F;
        double f = 520.1314;
        boolean g = true;
        char h = 'A';
        String name = "张三";
        String country = "中国";
        System.out.println(name + "来自" + country);
    }
}
